import 'package:flutter/material.dart';

class colorTheme {
  static primary(BuildContext context) {
    return Colors.teal[700];
  }
  static secondary(BuildContext context) {
    return Colors.teal;
  }
  static lightTeal(BuildContext context) {
    return Colors.teal[300];
  }
  static white(BuildContext context) {
    return Colors.white;
  }
}